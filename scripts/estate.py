# ── in-file generation markers (evidence source #2) ────────────────────────
# Empirically verified 2026-07-22: this regex over the first 480 bytes hits
# exactly the generated surfaces (126 files) and zero prose/generator files.

MARK = re.compile(
    r'(?:AUTO-)?GENERATED by|generated by design/build\.mjs|"_generated"|"derived_from"'
)
TOOL_IN_MARK = re.compile(r"((?:scripts|design)/[A-Za-z0-9_@./-]+\.(?:mjs|py))")
GATE_IN_MARK = re.compile(r"(?:Drift gates?|Gates?):\s*([^\n]+)")


def head_chunk(body: bytes) -> str:
    return body[:480].decode("utf-8", "replace")


def marker_hit(chunk: str) -> bool:
    return MARK.search(chunk) is not None


def marker_tool(chunk: str) -> str | None:
    m = re.search(r"GENERATED by(.{0,90})", chunk, re.S)
    if m:
        t = TOOL_IN_MARK.search(m.group(1))
        if t:
            return t.group(1)
    if "generated by design/build.mjs" in chunk:
        return "design/build.mjs"
    return None


def marker_gate(chunk: str) -> str | None:
    m = GATE_IN_MARK.search(chunk)
    if not m:
        return None
    return m.group(1).strip().rstrip(",").removesuffix(" and").rstrip(".").strip()


