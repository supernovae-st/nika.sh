# estate.py — the provenance manifest: every tracked file declares what it IS.
#
# OBSERVATION MODE (E0): estate.yaml declares provenance — authored ·
# generated · pinned-copy · foreign — it enforces nothing. The point is to
# make the repo's derivation topology explicit: which files a human writes,
# which files a tool re-derives (and from what, gated by which CI check),
# so drift between "what we believe" and "what is" becomes observable.
#
# SCHEMA 2 (pattern manifest). This tree tracks ~950 files — a per-file
# manifest (schema 1, the nika-registry shape) would bury the signal. Schema
# 2 adds `patterns:`: homogeneous groups (one glob → one class + shared
# evidence/derivation) carrying a member count and an AGGREGATE sha256
# (sha256 of the sorted "path sha256-of-bytes" lines of the members) — any
# member byte-change OR membership change re-renders differently, so --check
# still catches every drift at file granularity. `files:` lists per-file
# entries: exact-path overrides and evidence-bearing singletons (generated /
# pinned exceptions), which are matched BEFORE patterns.
#
# Classification is EVIDENCE-driven, never assumed:
#   1. scripts/spec-resync.contract.json — the repo's own machine-readable
#      derivation contract (16 sha256-pinned generators · 21 pinned outputs ·
#      the exact nika-spec commit) is read directly; its `runtime: copy`
#      twins land in `pinned-copy`.
#   2. In-file generation markers ("AUTO-GENERATED by <tool>" · "generated
#      by design/build.mjs" · "_generated" keys) are read from the first
#      bytes of each file; the tool AND its declared drift gate are
#      extracted from the marker itself where present.
#   3. Files with no such evidence land in `authored` with
#      `note: unverified-default` — honesty over completeness.
#
# Derived, never authored (the house projection pattern): --write
# regenerates estate.yaml from the tracked tree; --check re-emits and
# byte-compares. On divergence --check exits 5 (the ssot-compiler
# convention — deliberately distinct from the sibling drift gates' exit 1,
# so an estate divergence is distinguishable in CI logs).
#
# Usage:
#   python3 scripts/estate.py --write   # regenerate estate.yaml
#   python3 scripts/estate.py --check   # drift gate (exit 5 on divergence)

