def tracked_files() -> list:
    out = subprocess.run(
        ["git", "-C", str(ROOT), "ls-files", "-z"], capture_output=True, check=True
    )
    files = {f for f in out.stdout.decode().split("\0") if f}
    # The generator always classifies itself; the manifest never lists
    # itself (its sha256 cannot contain its own hash).
    files.add(SELF)
    files.discard("estate.yaml")
    return sorted(files)


# ── per-file overrides (exact path · matched before patterns) ──────────────


def auth(evidence: str, note: str | None = None) -> dict:
    out = {"class": "authored", "evidence": evidence}
    if note:
        out["note"] = note
    return out


def gen(evidence: str, tool: str, gate: str, inputs: list, note: str | None = None) -> dict:
    out = {
        "class": "generated",
        "evidence": evidence,
        "derivation": {"tool": tool, "gate": gate, "inputs": inputs},
    }
    if note:
        out["note"] = note
    return out


def pin(evidence: str, tool: str, gate: str, inputs: list, note: str | None = None) -> dict:
    out = gen(evidence, tool, gate, inputs, note)
    out["class"] = "pinned-copy"
    return out


