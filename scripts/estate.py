# ── the spec-resync contract outputs (evidence source #1) ──────────────────


def contract_entries(contract: dict) -> dict:
    spec = contract["spec"]
    spec_ref = f"{spec['repository']}@{spec['commit'][:12]}"
    resync_gate = (
        f"{RESYNC_YML} + pnpm spec-resync:verify (LENS-011 scripts/verify_generated_outputs.mjs — "
        f"literal path set AND byte digests vs {CONTRACT_PATH})"
    )
    out = {}
    for g in contract["generators"]:
        root = g["root"]
        runtime = g["runtime"]
        for path in g["outputs"]:
            if runtime == "copy":
                src = f"{spec_ref}:{g['path']}" if root == "spec" else g["path"]
                out[path] = {
                    "class": "pinned-copy",
                    "evidence": f"resync contract generator '{g['id']}' (runtime: copy) — byte-copy of {src} at the resync pin",
                    "derivation": {
                        "tool": "node scripts/spec-resync-run.mjs (the pinned resync)",
                        "gate": resync_gate,
                        "inputs": [".github/nika-spec-pin.json", src],
                    },
                }
            else:
                tool_repo = spec_ref if root == "spec" else "supernovae-st/nika.sh"
                out[path] = {
                    "class": "generated",
                    "evidence": f"declared output of resync contract generator '{g['id']}' ({tool_repo} {g['path']} · generator sha256-pinned in the contract)",
                    "derivation": {
                        "tool": f"{g['runtime']} {g['path']} (root: {root} · via node scripts/spec-resync-run.mjs)",
                        "gate": resync_gate,
                        "inputs": [".github/nika-spec-pin.json", f"{spec_ref} (tree {spec['tree'][:12]})"],
                    },
                }
    return out


# ── classification pipeline ────────────────────────────────────────────────


def classify_generic(rel: str, chunk: str) -> dict:
    if marker_hit(chunk):
        tool = marker_tool(chunk)
        if tool:
            gate = marker_gate(chunk) or TOOL_GATES.get(tool, GATE_FALLBACK)
            inputs = TOOL_INPUTS.get(tool, [f"declared in the {tool} header"])
            first = next(
                (l.strip() for l in chunk.splitlines() if MARK.search(l)), ""
            )
            return {
                "class": "generated",
                "evidence": f"in-file marker: {first[:160]}",
                "derivation": {"tool": tool, "gate": gate, "inputs": inputs},
            }
        return {
            "class": "generated",
            "evidence": "in-file generation marker present (tool not mechanically extractable)",
            "derivation": {
                "tool": "unknown — see the file's own header",
                "gate": GATE_FALLBACK,
                "inputs": ["see the file's own header"],
            },
            "note": "marker-without-tool — tighten the extractor or add an override",
        }
    return {
        "class": "authored",
        "evidence": "no generation marker or known role found",
        "note": "unverified-default",
    }


def q(s) -> str:
    """A JSON string is a valid YAML scalar — deterministic quoting for free."""
    return json.dumps(s, ensure_ascii=False)


def render() -> str:
    contract = load_contract()
    engine = shipped_engine()
    spec = contract["spec"]
    overrides = build_overrides(contract, engine)
    contract_rows = contract_entries(contract)
    patterns = build_patterns(contract, engine)
    for p in patterns:
        p["_re"] = glob_to_re(p["glob"])
        p["_members"] = []

    singles = []
    counts = {c: 0 for c in CLASSES}
    unverified = 0

    for rel in tracked_files():
        body = (ROOT / rel).read_bytes()
        sha = hashlib.sha256(body).hexdigest()
        chunk = head_chunk(body)

        row = None
        if rel in overrides:
            row = dict(overrides[rel])
        elif rel in contract_rows:
            row = dict(contract_rows[rel])
        else:
            for p in patterns:
                if p["_re"].match(rel) and ("when" not in p or p["when"](rel, chunk)):
                    p["_members"].append((rel, sha))
                    counts[p["class"]] += 1
                    break
            else:
                row = classify_generic(rel, chunk)

        if row is not None:
            row["path"] = rel
            row["sha256"] = sha
            counts[row["class"]] += 1
            if row.get("note") == "unverified-default":
                unverified += 1
            singles.append(row)

    singles.sort(key=lambda r: r["path"])
    live_patterns = [p for p in patterns if p["_members"]]

    lines = [
        "# GENERATED by scripts/estate.py — do not hand-edit.",
        "# The estate manifest: every tracked file declares its provenance.",
        "# OBSERVATION MODE (E0): this declares what IS — it enforces nothing.",
        "# SCHEMA 2 (pattern manifest — this tree tracks ~950 files):",
        "#   patterns: glob groups sharing one class/evidence/derivation; each",
        "#   carries its member count and an aggregate sha256 (sha256 of the",
        '#   sorted "path sha256" lines of its members) — any member byte-change',
        "#   or membership change diverges the render and --check exits 5.",
        "#   files: per-file entries — exact-path overrides and evidence-bearing",
        "#   singletons — matched BEFORE patterns.",
        "# Re-generate: python3 scripts/estate.py --write",
        "# Check:       python3 scripts/estate.py --check   (re-emits · byte-compares · exit 5 on divergence)",
        f"estate_schema: {ESTATE_SCHEMA}",
        "mode: observation",
        "repo: supernovae-st/nika.sh",
        f"spec_pin: {q(spec['repository'] + '@' + spec['commit'])}",
        f"shipped_engine: {q(engine)}",
        "classes:",
    ]
    for c in CLASSES:
        lines.append(f"  {c}: {q(CLASSES[c])}")
    lines.append("summary:")
    for c in CLASSES:
        lines.append(f"  {c}: {counts[c]}")
    lines.append(f"  unverified-default: {unverified}")
    lines.append(f"  files_listed: {len(singles)}")
    lines.append(f"  patterns: {len(live_patterns)}")
    lines.append(f"  files_total: {len(singles) + sum(len(p['_members']) for p in live_patterns)}")

    lines.append("patterns:")
    for p in live_patterns:
        members = sorted(p["_members"])
        agg = hashlib.sha256(
            ("".join(f"{rel} {sha}\n" for rel, sha in members)).encode()
        ).hexdigest()
        lines.append(f"- glob: {q(p['glob'])}")
        lines.append(f"  class: {p['class']}")
        lines.append(f"  count: {len(members)}")
        lines.append(f"  sha256: {agg}")
        lines.append(f"  evidence: {q(p['evidence'])}")
        if "derivation" in p:
            d = p["derivation"]
            lines.append("  derivation:")
            lines.append(f"    tool: {q(d['tool'])}")
            lines.append(f"    gate: {q(d['gate'])}")
            lines.append("    inputs:")
            for i in d["inputs"]:
                lines.append(f"    - {q(i)}")
        if p.get("note"):
            lines.append(f"  note: {q(p['note'])}")

    lines.append("files:")
    for row in singles:
        lines.append(f"- path: {q(row['path'])}")
        lines.append(f"  class: {row['class']}")
        lines.append(f"  sha256: {row['sha256']}")
        lines.append(f"  evidence: {q(row['evidence'])}")
        if "derivation" in row:
            d = row["derivation"]
            lines.append("  derivation:")
            lines.append(f"    tool: {q(d['tool'])}")
            lines.append(f"    gate: {q(d['gate'])}")
            lines.append("    inputs:")
            for i in d["inputs"]:
                lines.append(f"    - {q(i)}")
        if "note" in row:
            lines.append(f"  note: {q(row['note'])}")
    return "\n".join(lines) + "\n"


def main() -> int:
    mode = sys.argv[1] if len(sys.argv) > 1 else "--check"
    if mode not in ("--write", "--check"):
        print(f"estate.py: unknown mode {mode!r} (--write | --check)", file=sys.stderr)
        return 2
    rendered = render()
    if mode == "--write":
        ESTATE.write_text(rendered)
        n_f = rendered.count("\n- path: ")
        n_p = rendered.count("\n- glob: ")
        print(f"✓ estate.yaml · {n_f} files listed + {n_p} patterns")
        return 0
    if not ESTATE.is_file() or ESTATE.read_text() != rendered:
        print(
            "✗ estate drift · estate.yaml diverges from the tracked tree — run scripts/estate.py --write",
            file=sys.stderr,
        )
        return 5
    print("✓ estate.yaml in sync with the tracked tree")
    return 0


if __name__ == "__main__":
    sys.exit(main())
