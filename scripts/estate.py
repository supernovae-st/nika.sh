# ── pattern groups (glob → shared class/evidence/derivation) ───────────────


def glob_to_re(g: str) -> re.Pattern:
    out, i = [], 0
    while i < len(g):
        if g.startswith("**/", i):
            out.append(r"(?:.*/)?")
            i += 3
        elif g.startswith("**", i):
            out.append(r".*")
            i += 2
        elif g[i] == "*":
            out.append(r"[^/]*")
            i += 1
        elif g[i] == "?":
            out.append(r"[^/]")
            i += 1
        else:
            out.append(re.escape(g[i]))
            i += 1
    return re.compile("".join(out) + r"\Z")


def no_marker(rel: str, chunk: str) -> bool:
    return not marker_hit(chunk)


