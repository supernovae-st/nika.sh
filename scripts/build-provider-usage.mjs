#!/usr/bin/env node
/* ─── build-provider-usage · the per-provider room evidence ──────────────────
   Every /providers/<id> room shows the provider IN A REAL FILE. This
   compiler assembles that evidence, one entry per spec-named provider,
   from crafted donors at content/provider-usage/<id>.nika.yaml — each a
   complete minimal workflow whose infer tasks ride `model: <id>/<pin>`
   (the pinned upstream id, never a loose alias: the file stays
   reproducible after a vendor rotates names, and the audit can price it).

   Alongside the verbatim yaml, the compiler captures the AUDIT the
   released binary gives each donor (`nika check --json`): task and wave
   counts, whether the models resolve in THIS binary, and the static cost
   envelope. The room renders that verdict instead of claiming one — a
   cataloged-but-not-yet-runnable provider (moonshot today) says so
   plainly, never pretends (the honest-evidence law).

   Vendored-catalog law (build-terminal-captures precedent): the site
   build NEVER probes the binary — refresh is a deliberate manual run:
     node scripts/build-provider-usage.mjs
   Binary via NIKA_BIN, else `nika` on PATH. The vitest drift gate
   (provider-usage.test.ts) stays hermetic: it recompiles WITHOUT the
   binary (--from-capture reuses the committed audit block), byte-diffs,
   re-validates every donor against public/schema/workflow.json, and pins
   the donor id set against the catalog's.

   Determinism: same inputs → byte-identical output (providers in catalog
   order, no build stamps beyond the engine version the audit came from). */
import { execFileSync } from 'node:child_process'
import { readFileSync, writeFileSync, existsSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
const OUT = join(ROOT, 'src', 'content', 'provider-usage.generated.ts')
const DONOR_DIR = join(ROOT, 'content', 'provider-usage')
const fromCapture = process.argv.includes('--from-capture')

const catalog = JSON.parse(readFileSync(join(ROOT, 'public/providers/catalog.json'), 'utf8'))
const ids = catalog.providers.map((p) => p.id)

/* donors must cover the catalog exactly — a new provider without a donor
   fails THIS compiler loudly, never ships an empty room */
for (const id of ids) {
  if (!existsSync(join(DONOR_DIR, `${id}.nika.yaml`))) {
    console.error(`✖ missing donor: content/provider-usage/${id}.nika.yaml`)
    process.exit(1)
  }
}

/* the committed audit block survives hermetic recompiles (--from-capture):
   the drift gate re-emits from it byte-identically without the binary */
const prior = existsSync(OUT) ? readFileSync(OUT, 'utf8') : ''
const priorAudits = (() => {
  const m = prior.match(/export const PROVIDER_AUDITS[^=]*= (\{[\s\S]*?\n\}) as const/)
  return m ? JSON.parse(m[1]) : null
})()
const priorEngine = prior.match(/Engine at audit: "([^"]+)"/)?.[1] ?? null

let engine = priorEngine
let audits = priorAudits
if (!fromCapture) {
  const bin = process.env.NIKA_BIN || 'nika'
  engine = execFileSync(bin, ['--version'], { encoding: 'utf8', timeout: 5000 })
    .trim()
    .replace(/^nika\s+/, '')
  audits = {}
  for (const id of ids) {
    let raw
    try {
      raw = execFileSync(bin, ['check', join(DONOR_DIR, `${id}.nika.yaml`), '--json'], {
        encoding: 'utf8',
        timeout: 30000,
      })
    } catch (e) {
      /* a red audit still reports (exit != 0 carries the JSON on stdout) */
      raw = e.stdout?.toString() ?? ''
    }
    const r = JSON.parse(raw)
    audits[id] = {
      clean: r.clean === true,
      models_resolve: r.models_resolve === true,
      tasks: Array.isArray(r.certificate?.derivation) ? r.certificate.derivation.length : 0,
      waves: Array.isArray(r.waves) ? r.waves.length : 0,
      cost_ceiling_usd: r.cost?.has_unbounded ? null : (r.cost?.bounded_total_usd ?? null),
      cost_floor_usd: r.cost?.min_path_total_usd ?? 0,
    }
  }
} else if (!audits || !engine) {
  console.error('✖ --from-capture needs a committed provider-usage.generated.ts to reuse')
  process.exit(1)
}

const entries = ids.map((id) => ({
  id,
  yaml: readFileSync(join(DONOR_DIR, `${id}.nika.yaml`), 'utf8').replace(/\n+$/, '\n'),
  file: `${id}.nika.yaml`,
}))

const body = `// provider-usage.generated.ts — AUTO-GENERATED by scripts/build-provider-usage.mjs
// from content/provider-usage/*.nika.yaml (crafted donors · each audited by the
// released binary at regen time). DO NOT EDIT · regenerate:
//   node scripts/build-provider-usage.mjs
// Drift gate: src/test/provider-usage.test.ts recompiles hermetically
// (--from-capture), byte-diffs, and re-validates every donor against the
// served schema. Engine at audit: "${engine}".

/** the static audit the released binary gave the donor at regen time */
export interface ProviderAudit {
  /** every check leg green (moonshot ships false while the vendor is cataloged ahead of the runtime) */
  clean: boolean
  models_resolve: boolean
  tasks: number
  waves: number
  /** the worst-case ceiling in USD · null when any task is unpriced (local models are unpriced, never free) */
  cost_ceiling_usd: number | null
  cost_floor_usd: number
}

export interface ProviderUsage {
  id: string
  /** the donor, verbatim — a complete workflow, never a fragment */
  yaml: string
  /** the checked-in source of truth (content/provider-usage/) */
  file: string
}

export const PROVIDER_AUDITS: Record<string, ProviderAudit> = ${JSON.stringify(
  Object.fromEntries(ids.map((id) => [id, audits[id]])),
  null,
  2,
)} as const

export const PROVIDER_USAGE: Record<string, ProviderUsage> = ${JSON.stringify(
  Object.fromEntries(entries.map((e) => [e.id, e])),
  null,
  2,
)} as const
`

writeFileSync(OUT, body)
console.log(`✔ provider-usage.generated.ts · ${ids.length} donors · engine ${engine}`)
