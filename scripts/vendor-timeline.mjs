/* ─── vendor-timeline · the one verifiable record, vendored ──────────────────
   The spec repo's timeline/timeline.yaml is the SSOT of the project's
   history (eras · dated entries · forward gates that carry conditions,
   never dates) — every provable claim re-proven by the spec's own CI
   (timeline/verify.py). This script projects it into a typed module;
   the /timeline page renders the projection, never local prose.

   Run: NIKA_SPEC_ROOT=../spec/repo node scripts/vendor-timeline.mjs */
import { existsSync, readFileSync, writeFileSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'
import { parse } from 'yaml'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
const SPEC_ROOT =
  process.env.NIKA_SPEC_ROOT ??
  [
    join(ROOT, '..', 'spec', 'repo'),
    join(ROOT, '../../../../..', 'ventures/nika/02-engineering/repos/spec/repo'),
  ].find((p) => existsSync(join(p, 'timeline/timeline.yaml')))
if (!SPEC_ROOT) {
  console.error('vendor-timeline: no spec checkout with timeline/ found — set NIKA_SPEC_ROOT')
  process.exit(1)
}

const doc = parse(readFileSync(join(SPEC_ROOT, 'timeline/timeline.yaml'), 'utf8'))

/* evidence → a public proof href (null for labeled-unprovable classes) */
function proofHref(ev) {
  if (!ev) return null
  switch (ev.class) {
    case 'crates-io':
      return `https://crates.io/crates/${ev.crate}/versions`
    case 'github-release':
      return `https://github.com/${ev.repo}/releases/tag/${ev.tag}`
    case 'github-commit':
      return ev.sha ? `https://github.com/${ev.repo}/commit/${ev.sha}` : null
    case 'github-pr':
      return `https://github.com/${ev.repo}/pull/${ev.pr}`
    case 'scorecard':
      return `https://scorecard.dev/viewer/?uri=${ev.repo}`
    default:
      return null
  }
}

const PROVABLE = new Set(['git-tag', 'github-release', 'github-commit', 'github-pr', 'crates-io'])

const shape = (e) => ({
  date: String(e.date ?? ''),
  precision: e.precision ?? 'day',
  type: e.type ?? 'milestone',
  version: e.version ?? null,
  title: e.title ?? (e.version ? `${e.version}` : ''),
  detail: e.detail ?? null,
  component: e.component ?? null,
  evidence: {
    class: e.evidence?.class ?? 'testimony',
    provable: PROVABLE.has(e.evidence?.class),
    href: proofHref(e.evidence),
    note: e.evidence?.note ?? null,
  },
})

const out = {
  lastUpdated: doc.last_updated,
  eras: doc.eras.map((era) => ({
    id: era.id,
    span: era.span,
    title: era.title,
    story: era.story,
    evidenceClass: era.evidence?.class ?? 'testimony',
    entries: doc.entries.filter((e) => inEra(era.id, e.date)).map(shape),
  })),
  gates: doc.gates.map((g) => ({ id: g.id, title: g.title, conditions: g.conditions, note: g.note ?? null })),
}

function inEra(id, date) {
  const d = String(date)
  if (id === 'conception') return d < '2026-01-01'
  if (id === 'exploration') return d >= '2026-01-01' && d < '2026-04-13'
  return d >= '2026-04-13'
}

/* diamond genesis day: the rewrite entries land in diamond, but 0.79-era
   entries dated 2026-04-13 would too — the SSOT has none, by construction */

const banner = `/* GENERATED by scripts/vendor-timeline.mjs — DO NOT EDIT.
   Source of truth: nika-spec timeline/timeline.yaml (verified by the
   spec's own CI · timeline/verify.py). Regenerate: node scripts/vendor-timeline.mjs */`

writeFileSync(
  join(ROOT, 'src/content/timeline.generated.ts'),
  `${banner}\nexport const TIMELINE = ${JSON.stringify(out, null, 2)} as const\n`,
)
console.log(`✓ wrote src/content/timeline.generated.ts · ${doc.entries.length} entries · ${doc.gates.length} gates`)
