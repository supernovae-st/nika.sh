#!/usr/bin/env node
/* ── build-terminal-captures.mjs · the living terminal's recorded runs ───────
   WO-14 · W-A: the /proof terminal REPLAYS a whitelist of real commands —
   each one executed HERE against the released binary, captured verbatim,
   vendored. The site never runs anything: the terminal is a record player,
   and it says so on its face (the whitelist-honesty law: an off-list
   command answers with the teaching, never a fake shell).

   Vendored-catalog law (build-tools / build-anatomy precedent): the build
   NEVER probes the binary — refresh is a deliberate manual run:
     node scripts/build-terminal-captures.mjs
   Binary via NIKA_BIN, else `nika` on PATH. Deterministic entries only
   (no `run` — timings and side effects; no network). The whitelist is
   AUTHORED below; terminal.test.ts holds parity + provenance. */

import { execFileSync } from 'node:child_process'
import { mkdtempSync, writeFileSync, rmSync } from 'node:fs'
import { tmpdir } from 'node:os'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
const bin = process.env.NIKA_BIN || 'nika'

const version = execFileSync(bin, ['--version'], { encoding: 'utf8', timeout: 5000 })
  .trim()
  .replace(/^nika\s+/, '')

/* the fixture the commands run on — the install page's own hello (W2 · the
   file every visitor meets first) */
const HELLO = `nika: v1
workflow:
  id: hello
tasks:
  greet:
    exec:
      command: ["echo", "hello", "from", "nika"]
`

/* ── THE WHITELIST (authored · the terminal's whole vocabulary) ── */
const REGISTRY = [
  { id: 'version', command: 'nika --version', argv: ['--version'] },
  { id: 'check-hello', command: 'nika check hello.nika.yaml', argv: ['check', 'hello.nika.yaml'], file: HELLO },
  { id: 'inspect-hello', command: 'nika inspect hello.nika.yaml', argv: ['inspect', 'hello.nika.yaml'], file: HELLO,
    json_argv: ['inspect', 'hello.nika.yaml', '--format', 'json'] },
  { id: 'explain-parse-019', command: 'nika explain NIKA-PARSE-019', argv: ['explain', 'NIKA-PARSE-019'] },
  { id: 'explain-dag-003', command: 'nika explain NIKA-DAG-003', argv: ['explain', 'NIKA-DAG-003'] },
]

/* raw stdout → TermLine rows in the CLI's own voice (the TermFrame seam) */
function classify(raw) {
  return raw
    .replace(/\n+$/, '')
    .split('\n')
    .map((text) => {
      const t = text.trimStart()
      if (t.startsWith('✔')) return { kind: 'ok', text }
      if (t.startsWith('⚠')) return { kind: 'warn', text }
      if (t.startsWith('○') || t.startsWith('↳')) return { kind: 'soft', text }
      if (t.startsWith('wave ') || t.startsWith('▷') || t.startsWith('(')) return { kind: 'dim', text }
      return { kind: 'out', text }
    })
}

const work = mkdtempSync(join(tmpdir(), 'living-terminal-'))
const entries = []
for (const e of REGISTRY) {
  if (e.file) writeFileSync(join(work, 'hello.nika.yaml'), e.file)
  const env = { ...process.env, NO_COLOR: '1' }
  const raw = execFileSync(bin, e.argv, { encoding: 'utf8', timeout: 20000, cwd: work, env })
  const entry = { id: e.id, command: e.command, lines: classify(raw) }
  if (e.json_argv) {
    const rawJson = execFileSync(bin, e.json_argv, { encoding: 'utf8', timeout: 20000, cwd: work, env })
    entry.json = JSON.stringify(JSON.parse(rawJson), null, 1)
  }
  entries.push(entry)
}
rmSync(work, { recursive: true, force: true })

const ts = `// terminal-captures.generated.ts — AUTO-GENERATED by
// scripts/build-terminal-captures.mjs: the living terminal's recorded runs,
// executed against the released binary and captured VERBATIM (NO_COLOR).
// DO NOT EDIT · regenerate: node scripts/build-terminal-captures.mjs
// Gates: src/test/terminal.test.ts (whitelist parity · provenance · json twins).
import type { TermLine } from '../components/TermFrame'

export interface TerminalCapture {
  id: string
  /** the exact command line the visitor can replay */
  command: string
  /** the recorded output, in the CLI's own voice */
  lines: TermLine[]
  /** the machine twin (--format json) where the command has one */
  json?: string
}

/** the engine release every capture was recorded from */
export const TERMINAL_ENGINE = ${JSON.stringify(version)}

export const TERMINAL_CAPTURES: TerminalCapture[] = ${JSON.stringify(entries, null, 2)}
`
writeFileSync(join(ROOT, 'src/content/terminal-captures.generated.ts'), ts)
console.log(
  `wrote src/content/terminal-captures.generated.ts (${entries.length} recorded runs · engine ${version})`,
)
