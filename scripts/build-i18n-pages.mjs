#!/usr/bin/env node
/* ── build-i18n-pages.mjs · the L1 corpus → one typed module ─────────────────
   (WO-10 wiring · « les drafts sont la matière, LOCALIZED est le geste »)
   Compiles content/i18n/<locale>/<page>.yaml into a generated module the
   localized pages render from. The EN twin is a full member of the record:
   the page renders EVERY locale from the same source (manifesto-copy
   precedent — one shape, eight voices), so twin↔page drift cannot exist.

   Only pages whose EVERY announced locale has a reviewed file emit — the
   anti-slop law stays structural (the LOCALIZED registry announces, this
   module is what exists; i18n gates judge both directions).

   Deterministic: sorted locales, stable key order from the EN twin. */

import { readFileSync, readdirSync, writeFileSync, existsSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'
import { parse } from 'yaml'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
const I18N = join(ROOT, 'content/i18n')

const locales = readdirSync(I18N, { withFileTypes: true })
  .filter((d) => d.isDirectory())
  .map((d) => d.name)
  .sort()

const pages = readdirSync(join(I18N, 'en')).filter((f) => f.endsWith('.yaml'))

const out = {}
for (const file of pages) {
  const slug = file.replace(/\.yaml$/, '')
  out[slug] = {}
  for (const locale of locales) {
    const p = join(I18N, locale, file)
    if (!existsSync(p)) continue
    out[slug][locale] = parse(readFileSync(p, 'utf8'))
  }
}

const ts = `// i18n-pages.generated.ts — AUTO-GENERATED by scripts/build-i18n-pages.mjs
// from content/i18n/<locale>/*.yaml (the reviewed L1 corpus — the EN twin
// included: one shape, every voice, zero twin↔page drift by construction).
// DO NOT EDIT · regenerate: node scripts/build-i18n-pages.mjs
// Gates: src/test/i18n-lexicon.test.ts (corpus laws) · i18n.test.ts (routes).

export interface InstallStepCopy {
  n: string
  title: string
  plain: string
  plain2?: string
  ghost?: string
}
export interface InstallCopy {
  page: string
  html_title: string
  fig: string
  title: string
  punch: string
  lede: string
  stamp: string
  steps: {
    brew: InstallStepCopy
    script: InstallStepCopy
    manual: InstallStepCopy
    editor: InstallStepCopy
    agents: InstallStepCopy
    first_run: InstallStepCopy
  }
  capture_title: string
  trouble_title: string
  trouble: { q: string; a: string }[]
  more: { next: string; learn: string; usecases: string; docs: string }
  labels: Record<string, string>
}

export const I18N_PAGES: { install: Record<string, InstallCopy> } = ${JSON.stringify(out, null, 2)}
`
writeFileSync(join(ROOT, 'src/content/i18n-pages.generated.ts'), ts)
console.log(
  `wrote src/content/i18n-pages.generated.ts (${pages.length} page(s) × [${locales.join(', ')}])`,
)
