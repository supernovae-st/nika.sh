/* ─── vendor-lint-fixtures · the port's conformance corpus (WO-11 · U2) ──────
   The playground's TS lint (src/lib/nika-lint.ts) is a PINNED PORT of a
   subset of the engine's static checks — ratchet 8(b) requires it to
   REPLAY the spec's conformance fixtures in CI or it drifts silently
   (until today its only test was hand-written cases). This vendors the
   spec's core corpus (input.yaml + expected.json pairs · SPEC-TIME clock)
   into a test-only generated module; nika-lint-conformance.test.ts replays
   every case against lintNika. At I8 (the wasm oracle) the same corpus
   becomes the wasm parity gate.

   Source: the sibling spec clone (NIKA_SPEC_ROOT · same contract as the
   resync cron's builders). Deterministic: sorted ids, no dates.

   Run: NIKA_SPEC_ROOT=../spec node scripts/vendor-lint-fixtures.mjs */
import { existsSync, readFileSync, writeFileSync, readdirSync, statSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
/* env override first, then the sibling clone, then the monorepo layout
   (the build-templates.mjs probe pattern) — no checkout, die loudly */
const SPEC_ROOT =
  process.env.NIKA_SPEC_ROOT ??
  [
    join(ROOT, '..', 'spec', 'repo'),
    join(ROOT, '../../../../..', 'ventures/nika/02-engineering/repos/spec/repo'),
  ].find((p) => existsSync(join(p, 'conformance/tests/core')))
if (!SPEC_ROOT) {
  console.error('vendor-lint-fixtures: no spec checkout found — set NIKA_SPEC_ROOT')
  process.exit(1)
}

const CORE = join(SPEC_ROOT, 'conformance/tests/core')
const fixtures = []
for (const area of readdirSync(CORE).sort()) {
  const areaDir = join(CORE, area)
  if (!statSync(areaDir).isDirectory()) continue
  for (const caseId of readdirSync(areaDir).sort()) {
    const dir = join(areaDir, caseId)
    if (!statSync(dir).isDirectory()) continue
    const yaml = readFileSync(join(dir, 'input.yaml'), 'utf8')
    const expected = JSON.parse(readFileSync(join(dir, 'expected.json'), 'utf8'))
    /* an expected error names either an exact `code` or a bare `namespace`
       (the spec's own latitude: the engine picks the precise code) */
    fixtures.push({
      id: `${area}/${caseId}`,
      valid: expected.valid === true,
      codes: (expected.errors ?? []).flatMap((e) => (e.code ? [e.code] : [])).sort(),
      namespaces: (expected.errors ?? []).flatMap((e) => (e.namespace ? [e.namespace] : [])).sort(),
      yaml,
    })
  }
}
fixtures.sort((a, b) => a.id.localeCompare(b.id))

const body = `// lint-fixtures.generated.ts — AUTO-GENERATED by
// scripts/vendor-lint-fixtures.mjs from the nika-spec conformance core
// corpus (conformance/tests/core · input.yaml + expected.json pairs).
// DO NOT EDIT · regenerate: node scripts/vendor-lint-fixtures.mjs
// TEST-ONLY module: imported by nika-lint-conformance.test.ts alone —
// never by app code (the corpus must not enter any bundle).
// Drift gate: the test recompiles and byte-diffs; the resync cron
// re-vendors from its fresh spec clone.

export interface LintFixture {
  /** area/case (the spec's own tree) */
  id: string
  /** the spec's static verdict for this input */
  valid: boolean
  /** expected EXACT codes when invalid (sorted) */
  codes: string[]
  /** expected bare namespaces (the spec's latitude: any code within) */
  namespaces: string[]
  yaml: string
}

export const LINT_FIXTURES: LintFixture[] = ${JSON.stringify(fixtures, null, 2)}
`
writeFileSync(join(ROOT, 'src/lib/lint-fixtures.generated.ts'), body)
const invalid = fixtures.filter((f) => !f.valid).length
console.log(
  `wrote src/lib/lint-fixtures.generated.ts (${fixtures.length} fixtures · ${fixtures.length - invalid} valid · ${invalid} invalid)`,
)
